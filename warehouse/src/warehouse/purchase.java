    public void displaystock() throws SQLException{
        confirmationstock c = new confirmationstock();
            for (int i = 0; i <= c.productstock(); i++) {
                jTable2.setValueAt(c.displaystock(i, 0), i, 0);
                jTable2.setValueAt(c.displaystock(i, 1), i, 1);
                jTable2.setValueAt(c.displaystock(i, 2), i, 2);
            }   
    }
    
    public void tampilPembelian() throws SQLException {
        confirmation KB = new confirmation();
            for (int i = 0; i <= KB.amount(); i++) {
                jTable1.setValueAt(KB.displaypurchase(i, 0), i, 0);
                jTable1.setValueAt(KB.tampilPembelian(i, 1), i, 1);
        }
    }
